import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query';
import { useActor } from './useActor';
import type { Product, Donation, UserProfile, UserRole, ShoppingItem } from '../backend';

export type CheckoutSession = {
  id: string;
  url: string;
};

// Products
export function useGetAllProducts() {
  const { actor, isFetching } = useActor();

  return useQuery<Product[]>({
    queryKey: ['products'],
    queryFn: async () => {
      if (!actor) return [];
      return actor.getAllProducts();
    },
    enabled: !!actor && !isFetching,
  });
}

export function useCreateProduct() {
  const { actor } = useActor();
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async (product: { name: string; description: string; price: bigint; imageUrl: string }) => {
      if (!actor) throw new Error('Actor not available');
      return actor.createProduct(product.name, product.description, product.price, product.imageUrl);
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['products'] });
    },
  });
}

